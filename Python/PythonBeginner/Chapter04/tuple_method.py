tuple = ("hi", 123, 23.4, True) #in tuple we can store different data types
print(tuple)
a = tuple.count(123) # it will count how many times 123 is present in tuple
print(a)

b = tuple.index("hi") # it will return the index of first occurance of "hi"
print(b)

c = len(tuple)  # it will return length of tuple
print(c)